public class Task4 {
    public static void main(String[] args) {
        int[] nums = { 3, 2, 2, 3, 4, 3, 5, 100, 8, 25, 26, 26, 1, 4, 5 };
        int val = 3;
        int start = 0;
        int end = nums.length - 1;

        while (nums[end] == val) {
            end--;
        }
        while (start < end) {
            if (nums[start] == val) {
                int temp = nums[start];
                nums[start] = nums[end];
                nums[end] = temp;
                end--;
            } else {
                start++;
            }
        }

        // –í—ã–≤–æ–¥–∏–º –∏–∑–º–µ–Ω–µ–Ω–Ω—ã–π –º–∞—Å—Å–∏–≤ nums
        for (int num : nums) {
            System.out.print(num + " ");
        }
    }
}